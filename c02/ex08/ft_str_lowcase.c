char	*ft_strlowcase(char *str)
{
	int	i;

	if (str == '\0')
	{
		return (0);
	}
	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z') 
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
