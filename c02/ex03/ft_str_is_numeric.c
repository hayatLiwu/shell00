int	ft_str_is_numeric(char *str)
{
	while (*str)
	{
		while (*str >= '0' && *str <= '9')
		{
			str++;
		}
		if ((*str < 48 && *str != 0) || *str > 57)
			return (0);
	}
	return (1);
}
